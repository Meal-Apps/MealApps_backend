       $expense = Expense::create([
            'user_id' => $manager->id,
            'amount' => $validated['amount'],
            'description' => $validated['description'],
            'date' => $validated['date'],

        ]);

        return response()->json(['message' => 'Expense added successfully','expense' => $expense], 201);
    }

